#!/usr/bin/env node
import { readFile, writeFile } from "node:fs/promises";
import { join } from "node:path";
import { cwd } from "node:process";

const outputPath = join(cwd(), "src/data/generated/menu-categories.ts");

function parseDotEnv(contents) {
  const env = {};

  for (const line of contents.split(/\r?\n/)) {
    const trimmed = line.trim();

    if (!trimmed || trimmed.startsWith("#")) {
      continue;
    }

    const equalsIndex = trimmed.indexOf("=");
    if (equalsIndex === -1) {
      continue;
    }

    const key = trimmed.slice(0, equalsIndex).trim();
    let value = trimmed.slice(equalsIndex + 1).trim();

    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }

    env[key] = value;
  }

  return env;
}

function requireEnv(env, key) {
  const value = env[key] || process.env[key];

  if (!value) {
    throw new Error(`${key} is required in .env or the process environment.`);
  }

  return value;
}

function buildProductsUrl(siteUrl, page) {
  const url = new URL("/wp-json/wc/v3/products", siteUrl);

  url.searchParams.set("status", "publish");
  url.searchParams.set("per_page", "100");
  url.searchParams.set("page", String(page));
  url.searchParams.set("_fields", "id,status,categories");

  return url;
}

function categoryKey(category) {
  if (category.id !== undefined && category.id !== null && String(category.id).trim()) {
    return String(category.id);
  }

  return category.slug?.trim() || category.name?.trim() || "";
}

function buildCategories(products) {
  const categoriesById = new Map();

  for (const product of products) {
    if (product.status !== "publish") {
      continue;
    }

    const countedCategoryIds = new Set();

    for (const category of product.categories || []) {
      const id = categoryKey(category);
      const name = category.name?.trim();

      if (!id || !name || countedCategoryIds.has(id)) {
        continue;
      }

      countedCategoryIds.add(id);

      const existing = categoriesById.get(id);
      if (existing) {
        existing.productCount += 1;
        continue;
      }

      categoriesById.set(id, {
        id,
        name,
        slug: category.slug?.trim() || id,
        productCount: 1,
      });
    }
  }

  return Array.from(categoriesById.values()).filter(
    (category) => category.productCount > 0,
  );
}

async function fetchPublishedProducts({ siteUrl, consumerKey, consumerSecret }) {
  const authorization = `Basic ${Buffer.from(
    `${consumerKey}:${consumerSecret}`,
  ).toString("base64")}`;
  const products = [];
  let page = 1;
  let totalPages = 1;

  do {
    const response = await fetch(buildProductsUrl(siteUrl, page), {
      headers: {
        Accept: "application/json",
        Authorization: authorization,
      },
    });

    if (!response.ok) {
      throw new Error(`WooCommerce products request failed with HTTP ${response.status}.`);
    }

    const pageProducts = await response.json();

    if (!Array.isArray(pageProducts)) {
      throw new Error("WooCommerce products response was not an array.");
    }

    products.push(...pageProducts);

    const parsedTotalPages = Number(response.headers.get("x-wp-totalpages"));
    totalPages =
      Number.isFinite(parsedTotalPages) && parsedTotalPages > 0
        ? parsedTotalPages
        : page;
    page += 1;
  } while (page <= totalPages);

  return products;
}

function renderCategoriesModule(categories) {
  const rows = categories
    .map(
      (category) =>
        `  { id: ${JSON.stringify(category.id)}, name: ${JSON.stringify(
          category.name,
        )}, slug: ${JSON.stringify(category.slug)}, productCount: ${
          category.productCount
        } },`,
    )
    .join("\n");

  return `import type { MenuCategory } from "../royal-pizza";

// Generated by script/sync-woocommerce-categories.mjs from WooCommerce publish-only products.
export const syncedMenuCategories: MenuCategory[] = [
${rows}
];
`;
}

const env = parseDotEnv(await readFile(join(cwd(), ".env"), "utf8"));
const products = await fetchPublishedProducts({
  siteUrl: requireEnv(env, "WORDPRESS_SITE_URL"),
  consumerKey: requireEnv(env, "WOOCOMMERCE_CONSUMER_KEY"),
  consumerSecret: requireEnv(env, "WOOCOMMERCE_CONSUMER_SECRET"),
});
const categories = buildCategories(products);

await writeFile(outputPath, renderCategoriesModule(categories), "utf8");

console.log(
  `Synced ${categories.length} menu categories from ${products.length} published WooCommerce products.`,
);
